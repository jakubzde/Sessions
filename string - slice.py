base = "abcdefghijklmnopqrstuvwxyz"

print("here are some slices")
print(base[0:3]) #includes the beginning but not the ending character
print(base[:3])
print(base[10:])
print(base[10])
print(base[:10])
print(base[:]) #the whole sting
print(base[::2]) #every second character starting with the first [0] character
print(base[5:15:3]) #every third character from the 5th [6] to the 15th [7]
